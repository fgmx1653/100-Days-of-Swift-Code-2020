// Assignment Operator
let b = 10
var a = 5
a = b

// Arithmetic Operators
a + b  // Addition
a - b  // Subtraction
a * b  // Multiplication
a / b  // Division

// String concatenation
let h = "Hello, "
let w = "World!"
h + w // equals "Hello, World!"

// Remainder Operator
let nine = 9
let four = 4
let remainder = nine % four  // equals 1

// Compound Assignment Operators
a = 1
a += 9

// Comparison Operators
a == b  // Equal to
a != b  // Not equal to
a > b   // Greater than
a < b   // Less than (a < b)
a >= b  // Greater than or equal to (a >= b)
a <= b  // Less than or equal to (a <= b)

